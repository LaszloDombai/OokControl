function fPlotSig(inStr, inVect)
t = [0];
v = [1];
for C=2:length(inVect)
    t(C)= inVect(C) + t(C-1);
    v(C)= mod((v(C-1)+1),2);
end
figure;
stairs(t/1000,v,'LineWidth',2,'Marker','d','MarkerFaceColor','c');
grid on;
legend(inStr);
hold;
end

